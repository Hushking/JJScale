class Version{
    constructor(
        idVersao
        , autor
        , nota
        , dtFaturamento
        , valorFaturamento
        , dtAlteracao
        , versao
        , proposta
    ){
        this.idVersao = idVersao
        this.autor = autor
        this.nota = nota
        this.dtFaturamento = dtFaturamento
        this.valorFaturamento = valorFaturamento
        this.dtAlteracao = dtAlteracao
        this.versao = versao
        this.proposta = proposta
    }
}
module.exports = Version